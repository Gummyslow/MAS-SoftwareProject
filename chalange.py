def rolling_count(group: pd.DataFrame, window_hours: int) -> pd.Series:
    counts = []
    times  = group["timestamp"].values
    for i, t in enumerate(times):
        cutoff = t - np.timedelta64(window_hours, "h")
        counts.append(int((times[:i] >= cutoff).sum()))
    return pd.Series(counts, index=group.index)

