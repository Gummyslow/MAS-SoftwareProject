# Parse into structured records for easier downstream use
def parse_sms(raw: str) -> dict:
    lines  = raw.strip().split("\n")
    record = {"raw": raw, "sender": "", "recipient": "", "date": "", "body": ""}
    for line in lines:
        if line.startswith("From:"):
            record["sender"] = line.replace("From:", "").strip()
        elif line.startswith("To:"):
            record["recipient"] = line.replace("To:", "").strip()
        elif line.startswith("Date:"):
            record["date"] = line.replace("Date:", "").strip()
        elif line.startswith("Message:"):
            record["body"] = line.replace("Message:", "").strip()
    return record

