# Parse headers (From / To / Subject / Date) from raw email string
def parse_mail(raw: str) -> dict:
    record = {"raw": raw, "from": "", "to": "", "subject": "", "date": "", "body": ""}
    header_done = False
    body_lines  = []
    for line in raw.split("\n"):
        if header_done:
            body_lines.append(line)
        elif line.startswith("From:"):
            record["from"]    = line.replace("From:", "").strip()
        elif line.startswith("To:"):
            record["to"]      = line.replace("To:", "").strip()
        elif line.startswith("Subject:"):
            record["subject"] = line.replace("Subject:", "").strip()
        elif line.startswith("Date:"):
            record["date"]    = line.replace("Date:", "").strip()
        elif line.strip() == "":
            header_done = True   # blank line = end of headers
    record["body"] = " ".join(body_lines)
    return record

