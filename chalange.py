def compute_impossible_travel(group: pd.DataFrame) -> pd.Series:
    flags = [0] * len(group)
    rows  = group.reset_index()

    for i in range(1, len(rows)):
        curr = rows.iloc[i]
        prev = rows.iloc[i - 1]

        # Need GPS pings on both transactions
        if any(pd.isna([
            curr["ping_lat"], curr["ping_lng"],
            prev["ping_lat"], prev["ping_lng"]
        ])):
            continue

        dist_km   = haversine(
            prev["ping_lat"], prev["ping_lng"],
            curr["ping_lat"], curr["ping_lng"]
        )
        delta_h = (curr["timestamp"] - prev["timestamp"]).total_seconds() / 3600

        if delta_h <= 0:
            continue

        speed_kmph = dist_km / delta_h
        if speed_kmph > MAX_SPEED_KMPH and dist_km > 50:
            flags[i] = 1

    return pd.Series(flags, index=group.index)

