import time

def run_pipeline(df_input: pd.DataFrame, level: int = CURRENT_LEVEL) -> dict:
    t0 = time.time()
    print(f"\n{'='*55}")
    print(f"  PIPELINE START — Level {level} | {len(df_input):,} transactions")
    print(f"{'='*55}\n")

    df = df_input.copy()

    # ── Drop any previously computed columns to avoid duplicates ───────────────
    DERIVED_COLS = [
        "bt_firstname","bt_lastname","bt_city_code","bt_index","sender_type",
        "tx_type_enc","payment_method_enc","sender_type_enc",
        "sender_mean","sender_std","amount_zscore","balance_ratio","amount_vs_salary_ratio",
        "hour","day_of_week","month","is_weekend","is_night",
        "hour_sin","hour_cos","dow_sin","dow_cos",
        "prev_amount","prev_timestamp","time_since_prev_min","card_test_score",
        "new_recipient","merchant_novelty","merchant_novelty_rate",
        "tx_count_1h","tx_count_6h","tx_count_24h","amount_24h",
        "burst_score","amount_spike_score","hour_shift_score",
        "balance_drain_score","behavioral_score","payment_method_shift",
        "geo_anomaly_score","geo_distance_score_adj","impossible_travel",
        "city_mismatch","geo_outside_radius_score",
        "nlp_sms_score","nlp_mail_score","nlp_phishing_urls","nlp_risk_score",
        "net_new_node","net_hub_score","net_new_edge","net_recipient_pagerank_norm","network_score",
        "ensemble_score",
    ]
    df.drop(columns=[c for c in DERIVED_COLS if c in df.columns], inplace=True)

    # ── STAGE 1: Feature Engineering ───────────────────────────────────────────
    print("▶ Stage 1/5 — Feature Engineering...")

    df["transaction_type"] = df["transaction_type"].str.lower().str.strip()
    df["payment_method"]   = df["payment_method"].str.lower().str.strip()
    df["hour"]             = df["timestamp"].dt.hour
    df["day_of_week"]      = df["timestamp"].dt.dayofweek
    df["month"]            = df["timestamp"].dt.month
    df["is_weekend"]       = (df["day_of_week"] >= 5).astype(int)
    df["is_night"]         = ((df["hour"] >= 22) | (df["hour"] < 6)).astype(int)
    df["hour_sin"]         = np.sin(2 * np.pi * df["hour"] / 24)
    df["hour_cos"]         = np.cos(2 * np.pi * df["hour"] / 24)
    df["dow_sin"]          = np.sin(2 * np.pi * df["day_of_week"] / 7)
    df["dow_cos"]          = np.cos(2 * np.pi * df["day_of_week"] / 7)

    sender_stats = df.groupby("sender_id")["amount"].agg(
        sender_mean="mean", sender_std="std"
    ).fillna(0).reset_index()
    df = df.merge(sender_stats, on="sender_id", how="left")
    df["amount_zscore"] = (df["amount"] - df["sender_mean"]) / (df["sender_std"] + 1e-6)
    df["balance_ratio"] = df["amount"] / (df["balance_after"].abs() + 1e-6)

    # Safe salary lookup — user_salary may not exist in raw df_tx
    if "user_salary" in df.columns:
        df["amount_vs_salary_ratio"] = (df["amount"] / ((df["user_salary"] / 12) + 1e-6)).clip(upper=10)
    else:
        df["amount_vs_salary_ratio"] = 0.0

    # BioTag + sender type
    biotag_df         = df["sender_id"].apply(parse_biotag).apply(pd.Series)
    df                = pd.concat([df.reset_index(drop=True), biotag_df.reset_index(drop=True)], axis=1)
    df["sender_type"] = df["sender_id"].apply(classify_sender)

    df["tx_type_enc"] = le_type.transform(
        df["transaction_type"].fillna("unknown")
        .apply(lambda x: x if x in le_type.classes_ else "unknown")
    )
    df["payment_method_enc"] = le_pay.transform(
        df["payment_method"].fillna("unknown")
        .apply(lambda x: x if x in le_pay.classes_ else "unknown")
    )
    df["sender_type_enc"] = le_stype.transform(
        df["sender_type"].fillna("unknown")
        .apply(lambda x: x if x in le_stype.classes_ else "unknown")
    )
    print(f"   ✅ Features built")

    # ── STAGE 2: Behavioral Scoring ─────────────────────────────────────────────
    print("▶ Stage 2/5 — Behavioral Scoring...")
    df = df.sort_values(["sender_id", "timestamp"]).reset_index(drop=True)

    df["prev_amount"]         = df.groupby("sender_id")["amount"].shift(1)
    df["prev_timestamp"]      = df.groupby("sender_id")["timestamp"].shift(1)
    df["time_since_prev_min"] = (
        (df["timestamp"] - df["prev_timestamp"]).dt.total_seconds() / 60
    ).fillna(9999)
    df["card_test_score"] = df.apply(card_test_score, axis=1)

    seen = set()
    df["new_recipient"] = [
        0 if (pair := (row.sender_id, row.recipient_id)) in seen
        else (seen.add(pair) or 1)
        for row in df.itertuples()
    ]

    seen_m = {}
    novelty_vals = []
    for _, row in df.iterrows():
        key = str(row["recipient_id"]) + str(row.get("location", ""))
        sid = row["sender_id"]
        if sid not in seen_m:
            seen_m[sid] = set()
        novelty_vals.append(0 if key in seen_m[sid] else 1)
        seen_m[sid].add(key)
    df["merchant_novelty"] = novelty_vals

    df["merchant_novelty_rate"] = (
        df.groupby("sender_id")["merchant_novelty"]
        .transform(lambda x: x.rolling(5, min_periods=1).mean())
    )

    for col in ["tx_count_1h","tx_count_6h","tx_count_24h","amount_24h",
                "burst_score","amount_spike_score","hour_shift_score",
                "balance_drain_score","behavioral_score","payment_method_shift"]:
        df[col] = 0.0

    print(f"   ✅ Behavioral scores computed")

    # ── STAGE 3: Geo + NLP + Network ───────────────────────────────────────────
    print("▶ Stage 3/5 — Geo / NLP / Network...")

    # Geo — pull from df_master via transaction_id lookup
    for col in ["geo_anomaly_score","geo_distance_score_adj",
                "impossible_travel","city_mismatch","geo_outside_radius_score"]:
        if col in df_master.columns:
            mapping = df_master.set_index("transaction_id")[col].to_dict()
            df[col]  = df["transaction_id"].map(mapping).fillna(0.0)
        else:
            df[col] = 0.0

    # NLP — apply per row, then assign columns individually (no concat)
    nlp_result = df["bt_firstname"].apply(get_nlp_features)
    for col in ["nlp_sms_score","nlp_mail_score","nlp_phishing_urls","nlp_risk_score"]:
        df[col] = nlp_result[col].values

    # Network
    max_in_degree = max(in_degree.values()) + 1e-6  if in_degree  else 1.0
    max_pr        = max(pagerank.values())  + 1e-9   if pagerank   else 1.0

    df["net_new_node"]  = df["recipient_id"].apply(lambda r: 0 if r in seen_nodes else 1)
    df["net_hub_score"] = df["recipient_id"].apply(lambda r: in_degree.get(r, 0) / max_in_degree)
    df["net_new_edge"]  = df.apply(
        lambda row: 0 if (row["sender_id"], row["recipient_id"]) in seen_edges else 1, axis=1
    )
    df["net_recipient_pagerank_norm"] = df["recipient_id"].apply(
        lambda r: pagerank.get(r, 0.0) / max_pr
    )
    df["network_score"] = (
        df["net_new_node"]                * 0.30 +
        df["net_new_edge"]                * 0.25 +
        df["net_hub_score"]               * 0.25 +
        df["net_recipient_pagerank_norm"] * 0.20
    )
    print(f"   ✅ Geo / NLP / Network done")

    # ── STAGE 4: Ensemble Scoring ───────────────────────────────────────────────
    print("▶ Stage 4/5 — Ensemble Scoring...")

    xgb_ready = "xgb_model" in globals() and xgb_model is not None
    if xgb_ready:
        X     = df[FEATURE_COLS].fillna(0).values
        probs = np.zeros(len(df))
        mask  = (df["sender_type"] == "citizen").values
        probs[mask] = xgb_model.predict_proba(X[mask])[:, 1]
        df["ensemble_score"] = probs
    else:
        df["ensemble_score"] = df.apply(rule_based_score, axis=1)

    df.loc[df["sender_type"] != "citizen", "ensemble_score"] = 0.02
    print(f"   ✅ Scores computed | mean={df['ensemble_score'].mean():.3f}")

    # ── STAGE 5: LLM Gate ──────────────────────────────────────────────────────
    print("▶ Stage 5/5 — LLM Gate...")

    results   = {}
    llm_count = 0

    for _, row in df.iterrows():
        tx_id = row["transaction_id"]
        score = float(row["ensemble_score"])
        if score >= FRAUD_HARD:
            results[tx_id] = "FRAUD"
        elif score <= LEGIT_HARD:
            results[tx_id] = "LEGIT"
        else:
            ev = build_evidence_bundle(row)
            r  = llm_decide(tx_id, ev, use_strong=False)
            llm_count += 1
            if float(r["confidence"]) < LLM_CONFIDENCE_MIN:
                r = llm_decide(tx_id, ev, use_strong=True)
                llm_count += 1
            results[tx_id] = r["decision"]

    elapsed    = time.time() - t0
    fraud_list = [k for k, v in results.items() if v == "FRAUD"]

    print(f"\n{'='*55}")
    print(f"  PIPELINE COMPLETE — Level {level}")
    print(f"{'='*55}")
    print(f"  Transactions   : {len(results):,}")
    print(f"  FRAUD flagged  : {len(fraud_list):,} ({len(fraud_list)/len(results)*100:.1f}%)")
    print(f"  LLM calls      : {llm_count}")
    print(f"  Cost estimate  : ${FRAUD_MEMORY['cost_estimate_usd']:.4f}")
    print(f"  Runtime        : {elapsed:.1f}s")

    return results

