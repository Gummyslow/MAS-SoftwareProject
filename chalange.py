    time_ok   = 0 < row["time_since_prev_min"] < 60
    probe_ok  = row["prev_amount"] < 10
    large_ok  = ratio > 50
    if probe_ok and large_ok and time_ok:
        return 1.0
    if probe_ok and ratio > 20 and time_ok:
        return 0.7
    if ratio > 10 and time_ok:
        return 0.3
    return 0.0

